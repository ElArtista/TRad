#ifndef _TRIPACK_H_
#define _TRIPACK_H_

/************************************************************
 * A single header file 3D->2D rect triangle packing lib    *
 * author: Andreas Mantler (ands) | last change: 31.08.2016 *
 ************************************************************/

#ifndef TP_CALLOC
#define TP_CALLOC(count, size) calloc(count, size)
#endif

#ifndef TP_FREE
#define TP_FREE(ptr) free(ptr)
#endif

typedef int tp_bool;
#define TP_FALSE 0
#define TP_TRUE  1

/* Returns TP_FALSE if triangles do not fit into the rect
 * with the specified size, border and spacing */
tp_bool tp_pack_into_rect(const float* positions, int vertex_count,
                          int width, int height, int border, int spacing,
                          float* out_uvs, float* out_scale3Dto2D);

/* Returns number of successfully packed vertices */
int tp_pack_with_fixed_scale_into_rect(const float* positions, int vertex_count, float scale3Dto2D,
                                       int width, int height, int border, int spacing, float* out_uvs);

#endif /* ! _TRIPACK_H_ */
